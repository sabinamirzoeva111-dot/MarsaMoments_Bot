def detect_location(group_title: str | None) -> str | None:
    """По названию группы возвращает имя точки или None."""
    if not group_title:
        return None
    low = group_title.lower()
    for location, keywords in LOCATION_KEYWORDS.items():
        if any(kw in low for kw in keywords):
            return location
    return None


