def find_employee_by_name(name: str) -> dict | None:
    """Ищет сотрудника по имени или любому из алиасов (без учёта регистра).
    Возвращает запись из EMPLOYEES или None."""
    if not name:
        return None
    target = name.strip().lower()
    for emp_data in EMPLOYEES.values():
        if emp_data.get("name", "").lower() == target:
            return emp_data
        for alias in emp_data.get("aliases", []):
            if alias.lower() == target:
                return emp_data
    return None


def resolve_employee_name(username: str | None, phone: str | None = None,
                          fallback: str = "сотрудник") -> str:
    """По username или номеру телефона возвращает реальное имя.
    Если не нашли — возвращает fallback (например, first_name из Telegram)."""
    if username:
        # Telegram отдаёт username без @, но на всякий — снимаем
        u = username.lstrip("@")
        if u in EMPLOYEES:
            return EMPLOYEES[u]["name"]
    if phone:
        digits = "+" + "".join(c for c in phone if c.isdigit())
        if digits in EMPLOYEES:
            return EMPLOYEES[digits]["name"]
    return fallback


